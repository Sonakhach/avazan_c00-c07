void	ft_rev_int_tab(int *tab, int size)
{
	int	arr;
	int	i;

	i = 0;
	while (i < size / 2)
	{
		arr = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = arr;
		i++;
	}
}
