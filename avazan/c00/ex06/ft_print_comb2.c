#include <unistd.h>

void	print(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void )
{
	char	a;
	char	b;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			print((a / 10) + '0');
			print((a % 10) + '0');
			print(' ');
			print((b / 10) + '0');
			print((b % 10) + '0');
			if (a != 98)
				write(1, ", ", 2);
		b++;
		}
		a ++;
	}
}
